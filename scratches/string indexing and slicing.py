#string indexing
str="Hello world"
print(str[4])#the index position always start from 0
print(str[8])
print(str[-3])# we can also get the index from reverse of a string
#string slicing concepts
str="Abdullah Amjad"
print(str[0:8])
print(str[::2])#after the colon will read the string fully and increment by 2
#reversing string through slicing
print(str[-1::-1])
